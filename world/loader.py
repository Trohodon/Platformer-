import random


def load_demo_level(seed: int = None):
    """
    Hand-authored maps (coded in) to avoid pockets / unreachable areas.
    If seed is provided, chooses a deterministic map; otherwise random.
    """
    maps = [
        _MAP_1(),
        _MAP_2(),
        _MAP_3(),
    ]

    if seed is None:
        return random.choice(maps)

    rng = random.Random(seed)
    return maps[rng.randrange(len(maps))]


